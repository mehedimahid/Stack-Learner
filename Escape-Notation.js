var str = ' this is a \'String\''
console.log(str)

var str = ' this is a \nString'  //new line
console.log(str)


var str = ' this is a \tString'  //tab
console.log(str)


var str = ' this is a \vString'  
console.log(str)



/*

	Code	Output
	\'	single quote
	\"	double quote
	\\	backslash
	\n	new line
	\	r	carriage return
	\v	vertical tab
	\t	tab
	\b	backspace
	\f	form feed

*/